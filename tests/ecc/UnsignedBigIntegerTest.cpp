#include "gtest/gtest.h"
#include "../../includes/ecc/UnsignedBigInteger.h"

using ecc::UnsignedBigInteger;

TEST(UnsignedBigIntegerTest, addition) {
    UnsignedBigInteger a, b, c;
    a = 12;
    b = 13;
    c = 25;
    EXPECT_EQ(c, a + b);

    a = "5206915870124834899065441625155476344422";
    b = "632858552968765207938903017689760346846376725279197247389351";
    c = "632858552968765207944109933559885181745442166904352723733773";
    EXPECT_EQ(c, a + b);
}

TEST(UnsignedBigIntegerTest, subtraction) {
    UnsignedBigInteger a, b, c;
    a = 75;
    b = 25;
    c = 50;
    EXPECT_EQ(c, a - b);

    a = "52069158701248348990694216690437672525441625155476344422";
    b = "632858552968765207938910791993355988518174547247389351";
    c = "51436300148279583782755305898444316536923450608228955071";
    EXPECT_EQ(c, a - b);
}


TEST(UnsignedBigIntegerTest, multiplication) {
    UnsignedBigInteger a, b, c;
    a = 183;
    b = 96;
    c = 17568;
    EXPECT_EQ(c, a * b);

    a = "520691587670793891079199525441622795837827553058984443165351554765290696344422";
    b = "6328585520124834892421669043796873355988518174547247389351";
    c = "3295241242184197224320030900734076779249975516912524640613078849244426367791308551622519659152149227496619025568820747648521818631050122";
    EXPECT_EQ(c, a * b);
}